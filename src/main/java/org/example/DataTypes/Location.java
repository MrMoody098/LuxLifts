package org.example.DataTypes;


public class Location {
    private int x;
    private int y;

    public Location(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

   
    public int getY() {
        return y;
    }

    public int getX() {
        return this.x;
    }
    public int getY(){
        return this.y;
    }
    public void setX(){
        this.x = x;
    }
    public void setY(){
        this.y = y;
    }
}

